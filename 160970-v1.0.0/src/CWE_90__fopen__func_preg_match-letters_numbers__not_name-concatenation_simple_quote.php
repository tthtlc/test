    $tainted = "";
  }
  fclose($handle);
} else {
  $tainted = "";
}
$re = "/^[a-zA-Z0-9]*$/";
if(preg_match($re, $tainted) == 1){
  $tainted = $tainted;
}
else{
  $tainted = "";
}
